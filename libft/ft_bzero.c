#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*mem;

	mem = s;
	i = 0;
	while (mem[i] != '\0' && i < n)
	{
		mem[i] = '\0';
		i++;
	}
}
