#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	int				size;
	unsigned int	i;
	char			*dst;

	i = 0;
	size = ft_strlen(s1) + ft_strlen(s2);
	if (!(dst = malloc(sizeof(char) * (size + 1))))
		return (NULL);
	while (*s1 != '\0')
		dst[i++] = *s1++;
	while (*s2 != '\0')
		dst[i++] = *s2++;
	dst[i] = '\0';
	return (dst);
}
